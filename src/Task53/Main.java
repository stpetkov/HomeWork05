package Task53;

public class Main {
	static int multy(int a, int b, int c) {
		return (a * b * c);
	}

	public static void main(String[] args) {
		System.out.println(multy(12, 3, 7));
		System.out.println(multy(32, 5, 17));
		System.out.println(multy(89, 8, 45));
		System.out.println(multy(112, 4, 98));
		System.out.println(multy(121, 31, 9));
	}

}
